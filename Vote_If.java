class Vote_If
{
  public static void main(String[] args)
  {
    int age = 17;

    if (age >= 18)
    {
      System.out.println("Your age is : " + age + ", you are eligible to vote");

    }
    else
    {
      System.out.println("Your age is : " + age + ", you are not eligible to vote");
    }

  }

}
