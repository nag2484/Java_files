class Results_Switch
{
  public static void main(String[] args)
  {
    int caseNo = 1;

    switch(caseNo)
    {
      case 1:
        System.out.println("Result is FCD");
        break;
      case 2:
        System.out.println("Result is FC");
        break;
      case 3:
        System.out.println("Result is SC");
        break;
      default:
        System.out.println("Passed");
    }
  }
}
